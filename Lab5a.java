public class Lab5a {
    public static void main(String[] args){
        char score ='A';
        switch(score) {
        case 'A':
        System.out.println(10);
        break;
        case 'B':
        System.out.println(11);
        break;
        case 'C':
        System.out.println(112);
        break;
        case 'D':
        System.out.println(113);
        break;
        case 'E':
        System.out.println(114);
        break;
        case 'F':
        System.out.println(115);
        break;        
        default:
        System.out.println("error");
        }
    }
}  
